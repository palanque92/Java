package Gun08;

public class _05_JavaAssignmentOperators {
    public static void main(String[] args) {
        int a=10;

        a=a+5;    // a nın degerini 5 artırır
        a+=5;   // a nın artış degeri 5 , yukarıdakiyle aynı

        a=a-5;    // a nın degerini 5 azaltır
        a-=5;   // a nın azalma degeri 5 , yukarıdakiyle aynı

        a=a*5;    // a nın degerini 5 ile çarpar
        a*=5;   // a nın çarpanı 5 tir, yukarıdakiyle aynı

        a=a/5;    // a nın degerini 5 e böl
        a/=5;   // a nın bölen degeri 5 tir, yukarıdakiyle aynı
    }
}
